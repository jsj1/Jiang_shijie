#include <iostream>
#include <cmath>
using namespace std;
int main()
{
    double x=9.8,y=6.6;
    cout<<"ceil(x)="<<ceil(x)<<endl
    <<"cos(x)="<<cos(x)<<endl
    <<"exp(x)="<<exp(x)<<endl
    <<"fabs(x)="<<fabs(x)<<endl
    <<"floor(x)="<<floor(x)<<endl
    <<"fmod(x,y)="<<fmod(x,y)<<endl
    <<"log(x)="<<log(x)<<endl
    <<"log10(x)="<<log10(x)<<endl
    <<"pow(x,y)="<<pow(x,y)<<endl
    <<"sin(x)*x="<<sin(x)*x<<endl
    <<"sqrt(x)="<<sqrt(x)<<endl
    <<"tan(x)="<<tan(x)<<endl;
    return 0;
}
